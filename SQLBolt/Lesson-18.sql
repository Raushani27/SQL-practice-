-- from the database.

DROP TABLE IF EXISTS Database;

---------------------------------------------------------

Concepts Practiced:
• Deleting database tables
• Managing database schema
• Using DROP TABLE safely
• Working with DDL statements

Key Takeaways:
✔ DROP TABLE permanently removes a table
  and all of its data.
✔ IF EXISTS prevents an error if the table
  does not exist.
✔ DROP TABLE is a Data Definition Language (DDL)
  command used for schema management.
✔ Always verify that a table is no longer
  needed before deleting it.

